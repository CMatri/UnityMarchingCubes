    void BuildMesh()
    {
        List<CombineInstance> combine = new List<CombineInstance>();
        GetComponent<MeshFilter>().sharedMesh.Clear();
        if (points == null) return;

        for (int z = 0; z < worldSize - 1; z++)
        {
            for (int y = 0; y < worldSize - 1; y++)
            {
                for (int x = 0; x < worldSize - 1; x++)
                {
                    Vector3[] verts = new Vector3[12];
                    int idx = x + y * worldSize + z * worldSize * worldSize;
                    int lookup = 0;

                    if (points[idx] != null && points[idx].val < tolerance) lookup |= 128;
                    if (points[idx + 1] != null && points[idx+1].val < tolerance) lookup |= 64;
                    if (points[idx + 1 + worldSize] != null && points[idx+1+ worldSize].val < tolerance) lookup |= 4;
                    if (points[idx + worldSize] != null && points[idx+ worldSize].val < tolerance) lookup |= 8;
                    if (points[idx + (worldSize * worldSize)] != null && points[idx+(worldSize * worldSize)].val < tolerance) lookup |= 16;
                    if (points[idx + 1 + (worldSize * worldSize)] != null && points[idx+1+(worldSize * worldSize)].val < tolerance) lookup |= 32;
                    if (points[idx + 1 + worldSize + (worldSize * worldSize)] != null && points[idx+1+ worldSize + (worldSize * worldSize)].val < tolerance) lookup |= 2;
                    if (points[idx + worldSize + (worldSize * worldSize)] != null && points[idx+ worldSize + (worldSize * worldSize)].val < tolerance) lookup |= 1;
                    if(lookup != 0 && lookup != 255)
                    {
                        if ((Block.edgeTable[lookup] & 1) != 0) { verts[0] = Point.lerp(points[idx + worldSize + (worldSize * worldSize)], 
                                                                                        points[idx + 1 + worldSize + (worldSize * worldSize)]); }
                        if ((Block.edgeTable[lookup] & 2) != 0) { verts[1] = Point.lerp(points[idx + 1 + worldSize + (worldSize * worldSize)], 
                                                                                        points[idx + 1 + worldSize]); }
                        if ((Block.edgeTable[lookup] & 4) != 0) { verts[2] = Point.lerp(points[idx + 1 + worldSize], 
                                                                                        points[idx + worldSize]); }
                        if ((Block.edgeTable[lookup] & 8) != 0) { verts[3] = Point.lerp(points[idx + worldSize], 
                                                                                        points[idx + worldSize + (worldSize * worldSize)]); }
                        if ((Block.edgeTable[lookup] & 16) != 0) { verts[4] = Point.lerp(points[idx + worldSize * worldSize], 
                                                                                        points[idx + 1 + (worldSize * worldSize)]); }
                        if ((Block.edgeTable[lookup] & 32) != 0) { verts[5] = Point.lerp(points[idx + 1 + (worldSize * worldSize)], 
                                                                                        points[idx + 1]); }
                        if ((Block.edgeTable[lookup] & 64) != 0) { verts[6] = Point.lerp(points[idx + 1], 
                                                                                        points[idx]); }
                        if ((Block.edgeTable[lookup] & 128) != 0) { verts[7] = Point.lerp(points[idx], 
                                                                                        points[idx + (worldSize * worldSize)]); }
                        if ((Block.edgeTable[lookup] & 256) != 0) { verts[8] = Point.lerp(points[idx + worldSize + (worldSize * worldSize)], 
                                                                                        points[idx + (worldSize * worldSize)]); }
                        if ((Block.edgeTable[lookup] & 512) != 0) { verts[9] = Point.lerp(points[idx + 1 + worldSize + (worldSize * worldSize)], 
                                                                                        points[idx + 1 + (worldSize * worldSize)]); }
                        if ((Block.edgeTable[lookup] & 1024) != 0) { verts[10] = Point.lerp(points[idx + 1 + worldSize], 
                                                                                        points[idx + 1]); }
                        if ((Block.edgeTable[lookup] & 2048) != 0) { verts[11] = Point.lerp(points[idx + worldSize], 
                                                                                        points[idx]); }

                        List<Vector3> vertices = new List<Vector3>();
                        List<int> triangles = new List<int>();
                        int k = 0;
                        Mesh mesh = new Mesh();
                        for (int i = 0; Block.triTable[lookup, i] != -1; i += 3)
                        {
                            for (int j = i; j < i + 3; j++)
                            {
                                triangles.Add(k++);
                                vertices.Add(verts[Block.triTable[lookup, invertMesh ? (3 - (j - i) + i - 1) : j]]);
                            }

                            if (wireFrame)
                            {
                                Debug.DrawLine(verts[Block.triTable[lookup, i]], verts[Block.triTable[lookup, i + 1]]);
                                Debug.DrawLine(verts[Block.triTable[lookup, i + 1]], verts[Block.triTable[lookup, i + 2]]);
                                Debug.DrawLine(verts[Block.triTable[lookup, i + 2]], verts[Block.triTable[lookup, i]]);
                            }
                        }
                        mesh.vertices = vertices.ToArray();
                        mesh.triangles = triangles.ToArray();
                        CombineInstance cI = new CombineInstance() { mesh = mesh };
                        cI.transform = transform.localToWorldMatrix;
                        combine.Add(cI);
                    }
                }
            }
        }

        Mesh combinedMesh = new Mesh();
        combinedMesh.CombineMeshes(combine.ToArray());
        combinedMesh.RecalculateBounds();
        combinedMesh.RecalculateNormals();
        combinedMesh.RecalculateTangents();
        GetComponent<MeshFilter>().sharedMesh = combinedMesh;
    }

