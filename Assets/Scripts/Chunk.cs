    private Point?[] points;

    public void GeneratePoints(Vector3 offset, int worldSize, float scale, ITerrainGenerator generator, float noiseScale, Vector3 noiseOffset)
    {
        points = new Point?[worldSize * worldSize * worldSize];

        for (int z = 0; z < worldSize; z++)
        {
            for (int y = 0; y < worldSize; y++)
            {
                for (int x = 0; x < worldSize; x++)
                {
                    int idx = x + y * worldSize + z * worldSize * worldSize;
                    Vector3 pos = (new Vector3(x, y, z) + (offset * (worldSize - 1))) / (worldSize - 1);
                    Vector3 p = pos * scale;
                    points[idx] = new Point
                    {
                        x = p.x,
                        y = p.y,
                        z = p.z,
                        val = generator.genVal(noiseScale * (pos.x * (worldSize - 1) - (worldSize - 1) / 2f) + noiseOffset.x,
                        noiseScale * (pos.y * (worldSize - 1) - (worldSize - 1) / 2f + noiseOffset.y),
                        noiseScale * (pos.z * (worldSize - 1) - (worldSize - 1) / 2f) + noiseOffset.z)
                    };
                }
            }
        }
    }

    public void BuildMesh(Material mat, bool computeShader, bool doubleSidedMesh, bool invertMesh, bool wireFrame, float tolerance, ComputeShader chunkComputeShader, int worldSize)
    {
        if (points == null) return;
        Mesh result = new Mesh();
        result.indexFormat = UnityEngine.Rendering.IndexFormat.UInt32;

        if (computeShader)
        {
            ChunkShader.Triangle[] p = ChunkShader.RunShader(chunkComputeShader, points, worldSize, tolerance);

            var vertices = new Vector3[p.Length * 3];
            var meshTriangles = new int[p.Length * 3];

            for (int i = 0; i < p.Length; i++)
            {
                for (int j = 0; j < 3; j++)
                {
                    meshTriangles[i * 3 + j] = i * 3 + j;
                    vertices[i * 3 + j] = p[i][invertMesh ? 2 - j : j];
                }
            }

            result.vertices = vertices;
            result.triangles = meshTriangles;
        }
        else
        {
            List<CombineInstance> combine = new List<CombineInstance>();

            for (int z = 0; z < worldSize - 1; z++)
            {
                for (int y = 0; y < worldSize - 1; y++)
                {
                    for (int x = 0; x < worldSize - 1; x++)
                    {
                        Vector3[] verts = new Vector3[12];
                        int idx = x + y * worldSize + z * worldSize * worldSize;
                        if (idx >= points.Length) continue;
                        int lookup = 0;

                        if (points[idx] != null && points[idx].Value.val < tolerance) lookup |= 128;
                        if (points[idx + 1] != null && points[idx + 1].Value.val < tolerance) lookup |= 64;
                        if (points[idx + 1 + worldSize] != null && points[idx + 1 + worldSize].Value.val < tolerance) lookup |= 4;
                        if (points[idx + worldSize] != null && points[idx + worldSize].Value.val < tolerance) lookup |= 8;
                        if (points[idx + (worldSize * worldSize)] != null && points[idx + (worldSize * worldSize)].Value.val < tolerance) lookup |= 16;
                        if (points[idx + 1 + (worldSize * worldSize)] != null && points[idx + 1 + (worldSize * worldSize)].Value.val < tolerance) lookup |= 32;
                        if (points[idx + 1 + worldSize + (worldSize * worldSize)] != null && points[idx + 1 + worldSize + (worldSize * worldSize)].Value.val < tolerance) lookup |= 2;
                        if (points[idx + worldSize + (worldSize * worldSize)] != null && points[idx + worldSize + (worldSize * worldSize)].Value.val < tolerance) lookup |= 1;
                        if (lookup != 0 && lookup != 255)
                        {
                            if ((Block.edgeTable[lookup] & 1) != 0)
                            {
                                verts[0] = lerp(points[idx + worldSize + (worldSize * worldSize)],
                                                      points[idx + 1 + worldSize + (worldSize * worldSize)], tolerance);
                            }
                            if ((Block.edgeTable[lookup] & 2) != 0)
                            {
                                verts[1] = lerp(points[idx + 1 + worldSize + (worldSize * worldSize)],
                                                      points[idx + 1 + worldSize], tolerance);
                            }
                            if ((Block.edgeTable[lookup] & 4) != 0)
                            {
                                verts[2] = lerp(points[idx + 1 + worldSize],
                                                      points[idx + worldSize], tolerance);
                            }
                            if ((Block.edgeTable[lookup] & 8) != 0)
                            {
                                verts[3] = lerp(points[idx + worldSize],
                                                      points[idx + worldSize + (worldSize * worldSize)], tolerance);
                            }
                            if ((Block.edgeTable[lookup] & 16) != 0)
                            {
                                verts[4] = lerp(points[idx + worldSize * worldSize],
                                                     points[idx + 1 + (worldSize * worldSize)], tolerance);
                            }
                            if ((Block.edgeTable[lookup] & 32) != 0)
                            {
                                verts[5] = lerp(points[idx + 1 + (worldSize * worldSize)],
                                                     points[idx + 1], tolerance);
                            }
                            if ((Block.edgeTable[lookup] & 64) != 0)
                            {
                                verts[6] = lerp(points[idx + 1],
                                                     points[idx], tolerance);
                            }
                            if ((Block.edgeTable[lookup] & 128) != 0)
                            {
                                verts[7] = lerp(points[idx],
                                                    points[idx + (worldSize * worldSize)], tolerance);
                            }
                            if ((Block.edgeTable[lookup] & 256) != 0)
                            {
                                verts[8] = lerp(points[idx + worldSize + (worldSize * worldSize)],
                                                    points[idx + (worldSize * worldSize)], tolerance);
                            }
                            if ((Block.edgeTable[lookup] & 512) != 0)
                            {
                                verts[9] = lerp(points[idx + 1 + worldSize + (worldSize * worldSize)],
                                                    points[idx + 1 + (worldSize * worldSize)], tolerance);
                            }
                            if ((Block.edgeTable[lookup] & 1024) != 0)
                            {
                                verts[10] = lerp(points[idx + 1 + worldSize],
                                                   points[idx + 1], tolerance);
                            }
                            if ((Block.edgeTable[lookup] & 2048) != 0)
                            {
                                verts[11] = lerp(points[idx + worldSize],
                                                   points[idx], tolerance);
                            }

                            List<Vector3> vertices = new List<Vector3>();
                            List<int> triangles = new List<int>();
                            int k = 0;
                            Mesh mesh = new Mesh();
                            for (int i = 0; Block.triTable[lookup, i] != -1; i += 3)
                            {
                                for (int j = i; j < i + 3; j++)
                                {
                                    triangles.Add(k++);
                                    vertices.Add(verts[Block.triTable[lookup, invertMesh ? (3 - (j - i) + i - 1) : j]]);
                                }

                                if (wireFrame)
                                {
                                    Debug.DrawLine(verts[Block.triTable[lookup, i]], verts[Block.triTable[lookup, i + 1]]);
                                    Debug.DrawLine(verts[Block.triTable[lookup, i + 1]], verts[Block.triTable[lookup, i + 2]]);
                                    Debug.DrawLine(verts[Block.triTable[lookup, i + 2]], verts[Block.triTable[lookup, i]]);
                                }
                            }
                            mesh.vertices = vertices.ToArray();
                            mesh.triangles = triangles.ToArray();
                            CombineInstance cI = new CombineInstance() { mesh = mesh };
                            cI.transform = transform.localToWorldMatrix;
                            combine.Add(cI);
                        }
                    }
                }
            }

            result.CombineMeshes(combine.ToArray());
        }

        if (doubleSidedMesh)
        {
            var vertices = new Vector3[result.vertices.Length * 2];
            var meshTriangles = new int[result.triangles.Length * 2];
            Array.Copy(result.vertices, vertices, result.vertices.Length);
            Array.Copy(result.triangles, meshTriangles, result.triangles.Length);
            var meshTrianglesInv = result.triangles.Reverse().ToArray();
            Array.Copy(result.vertices, 0, vertices, result.vertices.Length, result.vertices.Length);
            Array.Copy(meshTrianglesInv, 0, meshTriangles, result.triangles.Length, result.triangles.Length);
            result.vertices = vertices;
            result.triangles = meshTriangles;
        }

        result.RecalculateBounds();
        result.RecalculateNormals();
        //result.RecalculateTangents();

        GetComponent<MeshFilter>().sharedMesh = result;
        GetComponent<MeshCollider>().sharedMesh = result;
        GetComponent<MeshRenderer>().material = mat;
    }

    private Vector3 lerp(Point? p1, Point? p2, float tolerance)
    {
        // return (p1.pos + p2.pos) / 2f;
        Vector3 ret = Vector3.zero;
        float diff = (tolerance - p1.Value.val) / (p2.Value.val - p1.Value.val);
        ret.x = p1.Value.x + (p2.Value.x - p1.Value.x) * diff;
        ret.y = p1.Value.y + (p2.Value.y - p1.Value.y) * diff;
        ret.z = p1.Value.z + (p2.Value.z - p1.Value.z) * diff;
        return ret;
    }

